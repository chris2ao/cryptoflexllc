// scripts/build-world-map.mjs
// Run: npx --package=topojson-client node scripts/build-world-map.mjs
// Requires land-110m.json in the project root (download from https://cdn.jsdelivr.net/npm/world-atlas@2/land-110m.json)
// Converts TopoJSON -> projected SVG path strings, writes src/lib/world-map-data.ts
import { readFileSync, writeFileSync } from "node:fs";
import { feature } from "topojson-client";

const topo = JSON.parse(readFileSync("land-110m.json", "utf-8"));
const geo = feature(topo, topo.objects.land);
const W = 1000;
const H = 500;

const project = (lon, lat) => [
  ((lon + 180) / 360) * W,
  ((90 - lat) / 180) * H,
];

const ringToPath = (ring) =>
  ring
    .map(([lon, lat], i) => {
      const [x, y] = project(lon, lat);
      return `${i === 0 ? "M" : "L"}${x.toFixed(1)},${y.toFixed(1)}`;
    })
    .join("") + "Z";

const paths = geo.features.flatMap((f) => {
  if (f.geometry.type === "Polygon") {
    return [f.geometry.coordinates.map(ringToPath).join("")];
  }
  if (f.geometry.type === "MultiPolygon") {
    return f.geometry.coordinates.map((poly) => poly.map(ringToPath).join(""));
  }
  return [];
});

const ts =
  `// Auto-generated by scripts/build-world-map.mjs\n` +
  `// World-atlas 110m land, equirectangular projection, W=1000 H=500. MIT/public-domain.\n` +
  `// Do not edit by hand -- re-run the script to regenerate.\n` +
  `export const WORLD_LAND_PATHS: readonly string[] = ${JSON.stringify(paths, null, 2)} as const;\n`;

writeFileSync("src/lib/world-map-data.ts", ts);
console.log(`Done: ${paths.length} land paths, ${ts.length} bytes written to src/lib/world-map-data.ts`);
